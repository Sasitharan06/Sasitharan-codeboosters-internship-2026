dept_data ={
    'dept_code' : ['CS','EC','ME','CIV'],
    'dept_name' : ['Computer Science','Electronics','Mechanical','Civil'],
    'hod_name' : ['Dr. A','Dr. B','Dr. C','Dr. D'],
    'established': [1985,1999,2006,2008],
    'intake': [60,60,69,37]
}

dept_data = pd.DataFrame(dept_data)
print(dept_data)

# Create a mapping from 'department' name to 'dept_code'
department_to_code_map = dict(zip(dept_data['dept_name'], dept_data['dept_code']))

# Apply the mapping to create a new 'dept_code' column in df
df['dept_code'] = df['department'].map(department_to_code_map)

# Store the department data in the SQLite database
dept_data.to_sql(
    'department',
    conn,
    if_exists='replace',
    index=False
)

# Recreate the 'students' table with the new 'dept_code' column
df.to_sql(
    'students',
    conn,
    if_exists='replace',
    index=False
)

print("\n'dept_code' column added to student DataFrame and 'department' and 'students' tables updated in database.")

