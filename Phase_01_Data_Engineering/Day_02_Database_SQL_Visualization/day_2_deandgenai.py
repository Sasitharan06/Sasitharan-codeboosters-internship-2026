def run_query(sql_query, description):
    """
    Executes a given SQL query using the global 'conn' object
    and returns the result as a pandas DataFrame.
    Prints a description before executing the query.
    """
    print(description)
    try:
        # 'conn' (sqlite connection object) is expected to be globally available from a previous cell.
        result_df = pd.read_sql_query(sql_query, conn)
        return result_df
    except NameError:
        print("Error: 'conn' object (SQLite connection) not found. Please ensure the database connection cell is executed.")
        return None
    except Exception as e:
        print(f"Error executing query: {e}")
        return None

